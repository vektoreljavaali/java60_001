package com.muhammet.temeloperatorler.artirmetikartirim;

public class Runner {

	public static void main(String[] args) {
		// arttýrým (++) sayý deðerini 1 arttýrmak için 
		int sayi=4;
		sayi = sayi + 1; // normal hali
		sayi += 5; // arttýrma atama hali
		sayi ++; // böyle kullanýn. :)
		System.out.println("sayi...: "+ sayi);
		// eksiltme (--)
		sayi--;
		System.out.println("sayi...: "+ sayi);

	}//Method Sonu

}//Class sonu
